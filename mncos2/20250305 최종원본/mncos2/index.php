<script>
$(document).ready(function() {
    // AOS 초기화 설정
    AOS.init({
        duration: 1000,
        easing: 'ease-out-cubic',
        once: false,  // 반복 실행되도록 변경
        offset: 100,
        delay: 100,
        disable: false
    });

    // URL 해시값 제거 (새로고침 시 첫 섹션으로 이동하기 위함)
    if (window.location.hash) {
        window.location.hash = '';
    }

    $('#main-container').fullpage({
        sectionSelector: '.section',
        autoScrolling: true,
        scrollHorizontally: false,
        navigation: true,
        navigationPosition: 'right',
        scrollingSpeed: 1000,
        easingcss3: 'cubic-bezier(0.645, 0.045, 0.355, 1)',
        fitToSection: true,
        css3: true,
        verticalCentered: false,
        menu: '#menu',
        anchors: ['slider', 'products', 'portfolio', 'history', 'news'],
        normalScrollElements: 'footer',
        
        afterRender: function() {
            setTimeout(() => {
                AOS.init({
                    duration: 1200,
                    easing: 'ease-out-cubic',
                    once: true,
                    offset: 50,
                    delay: 100,
                    disable: false
                });
                
                // 첫 섹션의 애니메이션만 실행
                document.querySelectorAll('#main-slider [data-aos]').forEach((element, i) => {
                    setTimeout(() => {
                        element.classList.add('aos-animate');
                    }, i * 200);
                });
            }, 500);
        },

        afterLoad: function(anchorLink, index) {
            const header = document.querySelector('.header');
            const topBtn = document.querySelector('.top-btn');

            // 헤더 처리
            if (index === 1) {
                header.classList.remove('scrolled');
                topBtn && topBtn.classList.remove('visible');
            } else {
                header.classList.add('scrolled');
                topBtn && topBtn.classList.add('visible');
            }

            // 현재 섹션의 AOS 요소들 애니메이션 실행
            const elements = document.querySelectorAll('.section.active [data-aos]');
            elements.forEach((element, i) => {
                setTimeout(() => {
                    element.classList.add('aos-animate');
                }, i * 200);
            });
        },

        onLeave: function(index, nextIndex, direction) {
            // 이전 섹션의 AOS 애니메이션 초기화
            document.querySelectorAll('.section:not(.active) [data-aos]').forEach(element => {
                element.classList.remove('aos-animate');
            });
        }
    });

    // 상단이동 버튼 클릭 이벤트
    $('.top-btn').on('click', function() {
        $.fn.fullpage.moveTo(1);
    });

    // 모바일 체크
    function checkMobile() {
        if (window.innerWidth <= 768) {
            $.fn.fullpage.destroy('all');
        } else {
            if ($('#main-container').fullpage) {
                $('#main-container').fullpage({
                    // 위와 동일한 옵션
                });
            }
        }
    }

    window.addEventListener('resize', checkMobile);
    checkMobile();

    // 메인 슬라이더 초기화
    let mainSlideInterval;

    function initMainSlider() {
        let currentSlide = 0;
        const slides = document.querySelectorAll('.main-slide');
        const totalSlides = slides.length;

        function showSlide(index) {
            slides.forEach(slide => slide.classList.remove('active'));
            slides[index].classList.add('active');
        }

        function changeSlide(direction) {
            if (direction === 'next') {
                currentSlide = (currentSlide + 1) % totalSlides;
            } else {
                currentSlide = (currentSlide - 1 + totalSlides) % totalSlides;
            }
            showSlide(currentSlide);
        }

        // 네비게이션 버튼 이벤트
        $('.main-prev').off('click').on('click', function() {
            changeSlide('prev');
            resetInterval();
        });

        $('.main-next').off('click').on('click', function() {
            changeSlide('next');
            resetInterval();
        });

        // 자동 슬라이드 시작
        function startInterval() {
            mainSlideInterval = setInterval(() => {
                changeSlide('next');
            }, 5000);
        }

        // 인터벌 리셋
        function resetInterval() {
            clearInterval(mainSlideInterval);
            startInterval();
        }

        // 첫 번째 슬라이드 표시 및 자동 슬라이드 시작
        showSlide(0);
        startInterval();
    }

    // 슬라이더 초기화
    initMainSlider();

    // 화면 크기 변경 시 슬라이더 재초기화
    $(window).on('resize', function() {
        initMainSlider();
    });
});
</script>
